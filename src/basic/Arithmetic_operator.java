package basic;

public class Arithmetic_operator {
	public static void main(String[] args) {
		int a = 10;//a,b,add,sub,mul,div are the integer datatype
		int b = 20;
		int add,sub,mul,div,mod;
		add =a+b;
		sub = a-b;
		mul = a*b;
		div = a/b;
		mod=a%b;
		System.out.println("Addition of Both values: "+add);
		System.out.println("Subraction of Both values: "+sub);
		System.out.println("Multiplication of Both values: "+mul);
		System.out.println("Division of Both values: "+div);
		System.out.println("Modulo of a and b is "+mod);
	}

}
